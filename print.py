# print using double quotes and single quotes
print("This is our first program in Python using double quotes")

print('This is our first program in Python using single quotes')

#print with quotes inside
print("He said, 'Python is Awesome!'")

print('She replied, "Indeed, it is!"')

#print numbers(no quotes)
print(30)
print(25)

#print multiple items
print("The answer is", 42) #,(comma)=> default seperator having value space
print("Sum of", 20, "and", 22, "is", 42)
print("I", "am", "Pranav")
print(20, 30, 90)

#print with custom seperator
#Sep => To add sep by our own
#we can use any string as a seperator
#we can use single double etc
print("Pranav", "LOVES", "coding", sep=">")

#printing without new line
#we can use single quotes or double for end values
#we can use any string as end value
print("Hello", end=" ")
print("World")

#print empty line
print()

#print using special character
